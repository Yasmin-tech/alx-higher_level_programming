#!/usr/bin/node
/*
 * a class Rectangle that defines a rectangle:
 * The constructor: take 2 arguments w and h
 * instances: attribute width with the value of w
 * attribute height with the value of h
 */

class Rectangle {
  constructor (w, h) {
    this.width = w;
    this.height = h;
  }
}
module.exports = Rectangle;
